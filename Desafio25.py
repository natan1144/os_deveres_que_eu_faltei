c = float(input('Insira um capital inicial: ')) # O usuário deverá inserir um capital inicial.
i = float(input('Insira uma taxa de juros: ')) # O usuário deverá inserir um taxa de juros.
t = float(input('Insira um tempo: ')) # O usuário deverá inserir um tempo/período.

j =  c*i*t # Foi efetuada a multiplicação do capital pela taxa de juros pelo tempo/período inserido.

print(f'|O juros simples calculado é igual a: {j}|') # Imprime o resultado dos juros simples.
