alt = float(input('Insira a altura do retângulo: ')) # O usuário deverá colocar a altura do retângulo.
larg = float(input('Insira a largura do retângulo: ')) # O usuário deverá colocar a largura do retângulo.

area = alt * larg # Foi efetuada a multiplicação dos valores.

print(f"|A área do seu retângulo é: {area}|") # Imprime a multiplicação de valores da altura e largura do retângulo que resulta no valor da área.