# Lista com 10 nomes variados.
lista_nomes = ['Felipe', 'Gabriel', 'Diogo', 'Thalita', 'Willian', 'Otávio', 'Jhonatan', 'Rodrigo', 'João', 'Vitória']

print(lista_nomes[0]) # Imprime o 1°(primeiro) elemento da lista.
print(lista_nomes[4]) # Imprime o 5°(quinto) elemento da lista.
print(lista_nomes[1:3]) # Imprime o 2° e 3°(segundo e terceiro) elemento da lista.
print(lista_nomes[:6]) # Imprime os 6(seis) últimos elementos da lista.
print(lista_nomes[7:]) # Imprime os 3(três) últimos elementos da lista.