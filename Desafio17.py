num1 = float(input('Insira um número: ')) # O usuário deverá inserir um número.
 
# Foi efetuada a soma/subtração no número inserido, ao número antecessor diminui 1(um) número, ao número sucessor se adiciona 1(um) número.
ant = num1 - 1 
suc = num1 + 1

print(f' O número antecessor ao que foi inserido é: {ant}') # Imprime o número antecessor ao número inserido. 
print(f' O número sucessor ao que foi inserido é: {suc}') # Imprime o número sucessor ao número inserido. 